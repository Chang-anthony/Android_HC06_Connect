void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim->Instance == TIM13) {
		//sensorData = HAL_GetTick();
		data.barometer += 100.0;
		data.height += 0.5;
		if (isConnect && recordFlag) {
				data.counter++;
			
				char buffer[BUFFER_SIZE];
				int len = sprintf(buffer, "%u,%.3f,%.3f\r\n", data.counter, data.barometer, data.height);
				HAL_UART_Transmit(&huart3, (uint8_t*)buffer, len, HAL_MAX_DELAY);
		}
	}		
}


void handleReceivedData(char* str) {
	 if (strcmp(str, "STOP") == 0) {
       recordFlag = false;
    } else if (strcmp(str, "START") == 0) {
       recordFlag = true;
			 data.counter = 0;
    } else if (strcmp(str, "RESETCOUNT") == 0){
			 data.counter = 0;
		} else{
			return;
    }
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if (huart->Instance == USART3) {
		
    // store str into buffer
    if (receivedData == '\n' || rxIndex >= BUFFER_SIZE - 1) {
        rxBuffer[rxIndex] = '\0';
        handleReceivedData(rxBuffer);
        rxIndex = 0;
     } else {
        rxBuffer[rxIndex++] = receivedData;
    }
    HAL_UART_Receive_IT(huart, (uint8_t*)&receivedData, 1);
	}
}


bool isDeviceConnected(void) {
    char atCmd[] = "AT\r\n";
    char response[10];
    HAL_UART_Transmit(&huart3, (uint8_t*)atCmd, sizeof(atCmd) - 1, HAL_MAX_DELAY);

    // get response
    HAL_UART_Receive(&huart3, (uint8_t*)response, sizeof(response) - 1, HAL_TIMEOUT);

    // check response "OK"
    if (strstr(response, "OK") != NULL) {
        return false; 
    } else {
        return true;
    }
}

